#include<stdio.h>

int main(){
	int a[5]={1,3,5,7,9};
	int *num[5]={&a[0],&a[1],&a[2],&a[3],&a[4]},i;
	
	// 数组指针保存了5个指针
	// 建立一个二级指针 即 指向指针的指针
	// 取数组指针的首地址
	// 首地址就是数组指针第一个位置的指针，赋给二级指针
	//  *p是数组指针的值 
	// **p是数组指针的值指向的地址的值 
	
	int **p;
	p=num;
	for(i=0;i<5;i++){
		printf("%p ",p);
		printf("%p ",*p);
		printf("%d ",**p);
		p++;
	}
	
} 
