#include<stdio.h>

int main(){
	int t,i,j,n;
	scanf("%d",&t);
	for(i=0;i<t;i++){
		scanf("%d",&n);
		for(j=1;j<=n;j++){
			if(j==7||(j%10==7)||(j/10%10==7)||(j/100%10==7)||(j/1000%10==7)||(j/10000%10==7)||(j%7==0)){
				printf("%d ",j);
			}	
			// %10 -> 求个位数的值
			// /10 取十位数 /100 取百位数 /1000取千位数 		
		}
		printf("\n");
	}
}
